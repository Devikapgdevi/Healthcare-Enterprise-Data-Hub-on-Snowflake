    $ENV AS ENVIRONMENT,
    $WH_COUNT AS WAREHOUSES_CREATED,
    $RM_COUNT AS RESOURCE_MONITORS_CREATED,
    $QUOTA_ACCOUNT AS TOTAL_MONTHLY_CREDIT_BUDGET,
    CASE WHEN $VALIDATION_PASSED THEN 'VALIDATED' ELSE 'VALIDATION_FAILED' END AS STATUS;

