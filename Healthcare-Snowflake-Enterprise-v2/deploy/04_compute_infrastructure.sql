-- STEP 6: COST GOVERNANCE VIEWS
-- ============================================================================

SET STEP_START = (SELECT CURRENT_TIMESTAMP());

USE DATABASE OPS_DB;
USE SCHEMA MONITORING;

-- Real-time credit usage view
CREATE OR REPLACE VIEW V_CREDIT_USAGE_REALTIME AS
SELECT
    WAREHOUSE_NAME,
    DATE_TRUNC('HOUR', START_TIME) AS USAGE_HOUR,
    SUM(CREDITS_USED) AS CREDITS_USED,
    SUM(CREDITS_USED_COMPUTE) AS CREDITS_COMPUTE,
    SUM(CREDITS_USED_CLOUD_SERVICES) AS CREDITS_CLOUD_SERVICES,
    COUNT(*) AS QUERY_COUNT
FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
WHERE START_TIME >= DATEADD(DAY, -7, CURRENT_TIMESTAMP())
    AND WAREHOUSE_NAME LIKE $DB_PREFIX || 'HC_%'
GROUP BY WAREHOUSE_NAME, DATE_TRUNC('HOUR', START_TIME)
ORDER BY USAGE_HOUR DESC;

-- Resource monitor status view
CREATE OR REPLACE VIEW V_RESOURCE_MONITOR_STATUS AS
SELECT
    NAME AS MONITOR_NAME,
    CREDIT_QUOTA AS MONTHLY_QUOTA,
    USED_CREDITS AS CREDITS_USED,
    REMAINING_CREDITS AS CREDITS_REMAINING,
    ROUND(100 * USED_CREDITS / NULLIF(CREDIT_QUOTA, 0), 2) AS PCT_USED,
    CASE 
        WHEN USED_CREDITS / NULLIF(CREDIT_QUOTA, 0) >= 0.9 THEN 'CRITICAL'
        WHEN USED_CREDITS / NULLIF(CREDIT_QUOTA, 0) >= 0.75 THEN 'WARNING'
        ELSE 'HEALTHY'
    END AS STATUS
FROM SNOWFLAKE.ACCOUNT_USAGE.RESOURCE_MONITORS
WHERE NAME LIKE $DB_PREFIX || 'HC_%';

-- Cost forecast view (simple linear projection)
CREATE OR REPLACE VIEW V_COST_FORECAST AS
WITH daily_usage AS (
    SELECT 
        DATE_TRUNC('DAY', START_TIME) AS USAGE_DATE,
        SUM(CREDITS_USED) AS DAILY_CREDITS
    FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
    WHERE START_TIME >= DATEADD(DAY, -30, CURRENT_TIMESTAMP())
        AND WAREHOUSE_NAME LIKE $DB_PREFIX || 'HC_%'
    GROUP BY DATE_TRUNC('DAY', START_TIME)
)
SELECT
    AVG(DAILY_CREDITS) AS AVG_DAILY_CREDITS,
    AVG(DAILY_CREDITS) * 30 AS PROJECTED_MONTHLY_CREDITS,
    AVG(DAILY_CREDITS) * DAY(LAST_DAY(CURRENT_DATE)) AS PROJECTED_THIS_MONTH,
    $QUOTA_ACCOUNT AS MONTHLY_BUDGET,
    ROUND(100 * (AVG(DAILY_CREDITS) * DAY(LAST_DAY(CURRENT_DATE))) / NULLIF($QUOTA_ACCOUNT, 0), 2) AS PROJECTED_PCT_OF_BUDGET
FROM daily_usage;

