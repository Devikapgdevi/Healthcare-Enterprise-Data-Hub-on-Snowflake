-- 13.1 ICU Feature Store (ML-ready features)
CREATE OR REPLACE TABLE ICU_FEATURE_STORE AS
SELECT
    p.PATIENT_ID,
    p.AGE,
    p.AGE_GROUP,
    p.GENDER,
    p.DIAGNOSIS,
    p.HOSPITAL_REGION,
    p.DAYS_SINCE_ADMISSION,
    p.TOTAL_ICU_EVENTS,
    p.CRITICAL_EVENTS,
    p.AVG_HEART_RATE,
    p.AVG_OXYGEN_LEVEL,
    p.AVG_SYSTOLIC_BP,
    p.AVG_DIASTOLIC_BP,
    p.MIN_OXYGEN_LEVEL,
    p.MAX_HEART_RATE,
    COALESCE(b.TOTAL_AMOUNT, 0) AS TOTAL_BILLING,
    COALESCE(b.PAID_AMOUNT, 0) AS PAID_AMOUNT,
    COALESCE(b.OVERDUE_AMOUNT, 0) AS OVERDUE_AMOUNT,
    -- Binary Risk Flags
    CASE WHEN p.AGE > 65 THEN 1 ELSE 0 END AS RISK_AGE,
    CASE WHEN p.AVG_OXYGEN_LEVEL < 92 THEN 1 ELSE 0 END AS RISK_LOW_OXYGEN,
    CASE WHEN p.AVG_HEART_RATE > 100 THEN 1 ELSE 0 END AS RISK_HIGH_HR,
    CASE WHEN p.CRITICAL_EVENTS > 5 THEN 1 ELSE 0 END AS RISK_CRITICAL_EVENTS,
    CASE WHEN p.AVG_SYSTOLIC_BP > 140 THEN 1 ELSE 0 END AS RISK_HIGH_BP,
    CASE WHEN p.DAYS_SINCE_ADMISSION > 14 THEN 1 ELSE 0 END AS RISK_LONG_STAY,
    CASE WHEN COALESCE(b.OVERDUE_AMOUNT, 0) > 10000 THEN 1 ELSE 0 END AS RISK_BILLING,
    -- Composite Risk Score
    (CASE WHEN p.AGE > 65 THEN 1 ELSE 0 END +
     CASE WHEN p.AVG_OXYGEN_LEVEL < 92 THEN 1 ELSE 0 END +
     CASE WHEN p.AVG_HEART_RATE > 100 THEN 1 ELSE 0 END +
     CASE WHEN p.CRITICAL_EVENTS > 5 THEN 1 ELSE 0 END +
     CASE WHEN p.AVG_SYSTOLIC_BP > 140 THEN 1 ELSE 0 END) AS RISK_SCORE,
    -- Risk Category
    CASE
        WHEN (CASE WHEN p.AGE > 65 THEN 1 ELSE 0 END +
              CASE WHEN p.AVG_OXYGEN_LEVEL < 92 THEN 1 ELSE 0 END +
              CASE WHEN p.AVG_HEART_RATE > 100 THEN 1 ELSE 0 END +
              CASE WHEN p.CRITICAL_EVENTS > 5 THEN 1 ELSE 0 END) >= 3 THEN 'CRITICAL'
        WHEN (CASE WHEN p.AGE > 65 THEN 1 ELSE 0 END +
              CASE WHEN p.AVG_OXYGEN_LEVEL < 92 THEN 1 ELSE 0 END +
              CASE WHEN p.AVG_HEART_RATE > 100 THEN 1 ELSE 0 END) >= 2 THEN 'HIGH'
        WHEN (CASE WHEN p.AGE > 65 THEN 1 ELSE 0 END +
              CASE WHEN p.AVG_OXYGEN_LEVEL < 92 THEN 1 ELSE 0 END) >= 1 THEN 'MEDIUM'
        ELSE 'LOW'
    END AS RISK_CATEGORY,
    CURRENT_TIMESTAMP AS FEATURE_TIMESTAMP
FROM ANALYTICS_DB.ANALYTICS_SCHEMA.PATIENT_ANALYTICS p
LEFT JOIN ANALYTICS_DB.ANALYTICS_SCHEMA.BILLING_ANALYTICS b ON p.PATIENT_ID = b.PATIENT_ID;

