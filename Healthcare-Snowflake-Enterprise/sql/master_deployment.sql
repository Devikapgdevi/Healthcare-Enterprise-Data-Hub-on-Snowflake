-- 7.1 High Credit Usage Alert
CREATE OR REPLACE ALERT HC_ALERT_HIGH_CREDITS
    WAREHOUSE = HC_ANALYTICS_WH
    SCHEDULE = 'USING CRON 0 8 * * * UTC'
    IF (EXISTS(
        SELECT 1 FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
        WHERE START_TIME >= DATEADD(DAY, -1, CURRENT_DATE)
        HAVING SUM(CREDITS_USED) > 20
    ))
    THEN CALL SYSTEM$SEND_EMAIL('healthcare_alerts', 'High Credit Usage', 'Daily credits exceeded 20');

-- 7.2 Failed Login Alert
CREATE OR REPLACE ALERT HC_ALERT_FAILED_LOGINS
    WAREHOUSE = HC_ANALYTICS_WH
    SCHEDULE = 'USING CRON */30 * * * * UTC'
    IF (EXISTS(
        SELECT 1 FROM SNOWFLAKE.ACCOUNT_USAGE.LOGIN_HISTORY
        WHERE IS_SUCCESS = 'NO' AND EVENT_TIMESTAMP >= DATEADD(HOUR, -1, CURRENT_TIMESTAMP)
        HAVING COUNT(*) > 5
    ))
    THEN CALL SYSTEM$SEND_EMAIL('healthcare_alerts', 'Failed Login Alert', 'Multiple failed logins detected');

-- 7.3 Long Running Query Alert
CREATE OR REPLACE ALERT HC_ALERT_LONG_QUERY
    WAREHOUSE = HC_ANALYTICS_WH
    SCHEDULE = 'USING CRON */15 * * * * UTC'
    IF (EXISTS(
        SELECT 1 FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
        WHERE TOTAL_ELAPSED_TIME > 1800000 AND START_TIME >= DATEADD(HOUR, -1, CURRENT_TIMESTAMP)
    ))
    THEN CALL SYSTEM$SEND_EMAIL('healthcare_alerts', 'Long Query Alert', 'Query running > 30 minutes');

-- 7.4 Warehouse Queue Alert
CREATE OR REPLACE ALERT HC_ALERT_QUEUE_TIME
    WAREHOUSE = HC_ANALYTICS_WH
    SCHEDULE = 'USING CRON */10 * * * * UTC'
    IF (EXISTS(
        SELECT 1 FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_LOAD_HISTORY
        WHERE AVG_QUEUED_LOAD > 5 AND START_TIME >= DATEADD(HOUR, -1, CURRENT_TIMESTAMP)
