-- 14.3 Deployment Procedure
CREATE OR REPLACE PROCEDURE SP_DEPLOY_SQL(
    P_DEPLOYMENT_ID STRING,
    P_SCRIPT_NAME STRING,
    P_PHASE_NUMBER NUMBER,
    P_SQL_SCRIPT STRING
)
RETURNS STRING
LANGUAGE JAVASCRIPT
EXECUTE AS CALLER
AS
$$
var start_time = Date.now();
try {
    var stmt = snowflake.createStatement({sqlText: P_SQL_SCRIPT});
    stmt.execute();
    var duration = Date.now() - start_time;
    
    var log_stmt = snowflake.createStatement({
        sqlText: "INSERT INTO DEVOPS_DB.CI_CD.DEPLOYMENT_LOG (DEPLOYMENT_ID, SCRIPT_NAME, PHASE_NUMBER, STATUS, DURATION_MS) VALUES (?, ?, ?, 'SUCCESS', ?)",
        binds: [P_DEPLOYMENT_ID, P_SCRIPT_NAME, P_PHASE_NUMBER, duration]
    });
    log_stmt.execute();
    
    var reg_stmt = snowflake.createStatement({
        sqlText: "INSERT INTO DEVOPS_DB.CI_CD.SCRIPT_REGISTRY (SCRIPT_ID, SCRIPT_NAME, PHASE_NUMBER) VALUES (?, ?, ?)",
        binds: [P_DEPLOYMENT_ID, P_SCRIPT_NAME, P_PHASE_NUMBER]
    });
    reg_stmt.execute();
    
    return 'SUCCESS: ' + duration + 'ms';
} catch(err) {
    var duration = Date.now() - start_time;
    var err_stmt = snowflake.createStatement({
        sqlText: "INSERT INTO DEVOPS_DB.CI_CD.DEPLOYMENT_LOG (DEPLOYMENT_ID, SCRIPT_NAME, PHASE_NUMBER, STATUS, ERROR_MESSAGE, DURATION_MS) VALUES (?, ?, ?, 'FAILED', ?, ?)",
        binds: [P_DEPLOYMENT_ID, P_SCRIPT_NAME, P_PHASE_NUMBER, err.message, duration]
    });
    err_stmt.execute();
    return 'FAILED: ' + err.message;
}
$$;

