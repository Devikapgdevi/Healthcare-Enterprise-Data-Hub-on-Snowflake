-- PHASE 6: MONITORING (10+ Consumption Insight Views)
-- ============================================================================

USE DATABASE MONITORING_DB;
USE SCHEMA MONITORING_SCHEMA;

-- 6.1 Query Performance View
CREATE OR REPLACE VIEW V_QUERY_PERFORMANCE AS
SELECT
    QUERY_ID, USER_NAME, ROLE_NAME, WAREHOUSE_NAME,
    EXECUTION_STATUS, ERROR_CODE, ERROR_MESSAGE,
    START_TIME, END_TIME, TOTAL_ELAPSED_TIME,
    BYTES_SCANNED, ROWS_PRODUCED, COMPILATION_TIME,
    CREDITS_USED_CLOUD_SERVICES
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE START_TIME >= DATEADD(DAY, -30, CURRENT_DATE);

-- 6.2 Warehouse Credit Usage View
CREATE OR REPLACE VIEW V_WAREHOUSE_CREDIT_USAGE AS
SELECT
    WAREHOUSE_NAME,
    DATE_TRUNC('DAY', START_TIME) AS USAGE_DATE,
    SUM(CREDITS_USED) AS TOTAL_CREDITS,
    SUM(CREDITS_USED_COMPUTE) AS COMPUTE_CREDITS,
    SUM(CREDITS_USED_CLOUD_SERVICES) AS CLOUD_CREDITS
FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
WHERE START_TIME >= DATEADD(DAY, -30, CURRENT_DATE)
GROUP BY WAREHOUSE_NAME, DATE_TRUNC('DAY', START_TIME);

-- 6.3 Daily Credit Summary View
CREATE OR REPLACE VIEW V_DAILY_CREDIT_SUMMARY AS
SELECT
    DATE_TRUNC('DAY', START_TIME) AS USAGE_DATE,
    SUM(CREDITS_USED) AS TOTAL_CREDITS,
    COUNT(DISTINCT WAREHOUSE_NAME) AS WAREHOUSES_USED
FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
WHERE START_TIME >= DATEADD(DAY, -30, CURRENT_DATE)
GROUP BY DATE_TRUNC('DAY', START_TIME);

-- 6.4 Login History View
CREATE OR REPLACE VIEW V_LOGIN_HISTORY AS
SELECT
    EVENT_TIMESTAMP, USER_NAME, CLIENT_IP,
    REPORTED_CLIENT_TYPE, FIRST_AUTHENTICATION_FACTOR,
    IS_SUCCESS, ERROR_CODE, ERROR_MESSAGE
FROM SNOWFLAKE.ACCOUNT_USAGE.LOGIN_HISTORY
WHERE EVENT_TIMESTAMP >= DATEADD(DAY, -30, CURRENT_DATE);

-- 6.5 Failed Logins View
CREATE OR REPLACE VIEW V_FAILED_LOGINS AS
SELECT *
FROM SNOWFLAKE.ACCOUNT_USAGE.LOGIN_HISTORY
WHERE IS_SUCCESS = 'NO'
    AND EVENT_TIMESTAMP >= DATEADD(DAY, -7, CURRENT_DATE);

-- 6.6 Long Running Queries View
CREATE OR REPLACE VIEW V_LONG_RUNNING_QUERIES AS
SELECT *
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE TOTAL_ELAPSED_TIME > 300000  -- 5 minutes
    AND START_TIME >= DATEADD(DAY, -7, CURRENT_DATE);

-- 6.7 Blocked/Failed Queries View
CREATE OR REPLACE VIEW V_FAILED_QUERIES AS
SELECT *
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE ERROR_CODE IS NOT NULL
    AND START_TIME >= DATEADD(DAY, -7, CURRENT_DATE);

-- 6.8 Storage Usage View
CREATE OR REPLACE VIEW V_STORAGE_USAGE AS
SELECT
    USAGE_DATE,
    DATABASE_NAME,
    AVERAGE_DATABASE_BYTES / (1024*1024*1024) AS SIZE_GB,
    AVERAGE_FAILSAFE_BYTES / (1024*1024*1024) AS FAILSAFE_GB
FROM SNOWFLAKE.ACCOUNT_USAGE.DATABASE_STORAGE_USAGE_HISTORY
WHERE USAGE_DATE >= DATEADD(DAY, -30, CURRENT_DATE);

-- 6.9 Warehouse Load History View
CREATE OR REPLACE VIEW V_WAREHOUSE_LOAD AS
SELECT
    WAREHOUSE_NAME, START_TIME, END_TIME,
    AVG_RUNNING, AVG_QUEUED_LOAD, AVG_BLOCKED
FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_LOAD_HISTORY
WHERE START_TIME >= DATEADD(DAY, -7, CURRENT_DATE);

-- 6.10 User Query Stats View
CREATE OR REPLACE VIEW V_USER_QUERY_STATS AS
SELECT
    USER_NAME,
    COUNT(*) AS TOTAL_QUERIES,
    SUM(CASE WHEN EXECUTION_STATUS = 'SUCCESS' THEN 1 ELSE 0 END) AS SUCCESSFUL,
    SUM(CASE WHEN EXECUTION_STATUS != 'SUCCESS' THEN 1 ELSE 0 END) AS FAILED,
    AVG(TOTAL_ELAPSED_TIME) AS AVG_DURATION_MS,
    SUM(CREDITS_USED_CLOUD_SERVICES) AS TOTAL_CREDITS
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE START_TIME >= DATEADD(DAY, -30, CURRENT_DATE)
GROUP BY USER_NAME;

-- 6.11 Data Transfer View
CREATE OR REPLACE VIEW V_DATA_TRANSFER AS
SELECT
    DATE_TRUNC('DAY', START_TIME) AS TRANSFER_DATE,
    SOURCE_CLOUD, SOURCE_REGION,
    TARGET_CLOUD, TARGET_REGION,
    SUM(BYTES_TRANSFERRED) / (1024*1024*1024) AS GB_TRANSFERRED
FROM SNOWFLAKE.ACCOUNT_USAGE.DATA_TRANSFER_HISTORY
WHERE START_TIME >= DATEADD(DAY, -30, CURRENT_DATE)
GROUP BY DATE_TRUNC('DAY', START_TIME), SOURCE_CLOUD, SOURCE_REGION, TARGET_CLOUD, TARGET_REGION;

-- 6.12 Materialized View Refresh View
CREATE OR REPLACE VIEW V_MV_REFRESH_HISTORY AS
SELECT
    DATABASE_NAME, SCHEMA_NAME, TABLE_NAME,
    START_TIME, END_TIME, CREDITS_USED,
    NUM_ROWS_INSERTED, NUM_ROWS_DELETED
FROM SNOWFLAKE.ACCOUNT_USAGE.MATERIALIZED_VIEW_REFRESH_HISTORY
WHERE START_TIME >= DATEADD(DAY, -30, CURRENT_DATE);

