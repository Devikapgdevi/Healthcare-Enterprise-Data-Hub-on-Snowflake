-- 7.5 Storage Growth Alert
CREATE OR REPLACE ALERT HC_ALERT_STORAGE_GROWTH
    WAREHOUSE = HC_ANALYTICS_WH
    SCHEDULE = 'USING CRON 0 6 * * * UTC'
