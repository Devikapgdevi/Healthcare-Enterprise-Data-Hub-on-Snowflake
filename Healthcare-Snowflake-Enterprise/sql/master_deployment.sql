    UNIFORM(18, 90, RANDOM()) AS AGE,
    CASE WHEN UNIFORM(0,1,RANDOM()) < 0.5 THEN 'M' ELSE 'F' END AS GENDER,
    DATEADD(DAY, -UNIFORM(0, 365, RANDOM()), CURRENT_DATE) AS ADMISSION_DATE,
    CASE UNIFORM(1,5,RANDOM())
        WHEN 1 THEN 'Cardiology'
        WHEN 2 THEN 'Neurology'
        WHEN 3 THEN 'Pulmonology'
        WHEN 4 THEN 'Orthopedics'
        ELSE 'General'
    END AS DIAGNOSIS,
    LPAD(UNIFORM(100000000, 999999999, RANDOM())::STRING, 9, '0') AS SSN,
    'INS-' || LPAD(UNIFORM(10000000, 99999999, RANDOM())::STRING, 8, '0') AS INSURANCE_NO,
    '(' || LPAD(UNIFORM(200, 999, RANDOM())::STRING, 3, '0') || ') ' ||
        LPAD(UNIFORM(200, 999, RANDOM())::STRING, 3, '0') || '-' ||
        LPAD(UNIFORM(1000, 9999, RANDOM())::STRING, 4, '0') AS PHONE,
    CASE UNIFORM(1,4,RANDOM())
        WHEN 1 THEN 'North'
        WHEN 2 THEN 'South'
        WHEN 3 THEN 'East'
        ELSE 'West'
    END AS HOSPITAL_REGION,
    CURRENT_TIMESTAMP AS LOAD_TIMESTAMP
FROM TABLE(GENERATOR(ROWCOUNT => 10000));

-- ICU Events (50,000 records)
CREATE OR REPLACE TABLE ICU_EVENTS AS
SELECT
