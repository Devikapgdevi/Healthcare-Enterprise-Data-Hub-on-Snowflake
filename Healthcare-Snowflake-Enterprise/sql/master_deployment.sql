-- 8.2 Masking Policy 1: SSN
CREATE OR REPLACE MASKING POLICY MASK_SSN
    AS (val STRING) RETURNS STRING ->
    CASE
        WHEN CURRENT_ROLE() IN ('ACCOUNTADMIN', 'HC_SECURITY_ADMIN') THEN val
        WHEN CURRENT_ROLE() IN ('HC_ACCOUNT_ADMIN') THEN '***-**-' || RIGHT(val, 4)
        ELSE '***-**-****'
    END
    COMMENT = 'SSN masking policy';

-- 8.3 Masking Policy 2: Phone
CREATE OR REPLACE MASKING POLICY MASK_PHONE
    AS (val STRING) RETURNS STRING ->
    CASE
        WHEN CURRENT_ROLE() IN ('ACCOUNTADMIN', 'HC_SECURITY_ADMIN') THEN val
        WHEN CURRENT_ROLE() IN ('HC_ACCOUNT_ADMIN', 'HC_DATA_ENGINEER') THEN '(***) ***-' || RIGHT(val, 4)
        ELSE '(***) ***-****'
    END
    COMMENT = 'Phone number masking policy';

-- 8.4 Masking Policy 3: Insurance Number
CREATE OR REPLACE MASKING POLICY MASK_INSURANCE
    AS (val STRING) RETURNS STRING ->
    CASE
        WHEN CURRENT_ROLE() IN ('ACCOUNTADMIN', 'HC_SECURITY_ADMIN') THEN val
        ELSE 'INS-****-' || RIGHT(val, 4)
    END
    COMMENT = 'Insurance number masking policy';

-- 8.5 Row Access Policy
CREATE OR REPLACE ROW ACCESS POLICY REGION_ACCESS_POLICY
    AS (hospital_region STRING) RETURNS BOOLEAN ->
    CASE
        WHEN CURRENT_ROLE() IN ('ACCOUNTADMIN', 'HC_SECURITY_ADMIN', 'HC_ACCOUNT_ADMIN') THEN TRUE
        WHEN CURRENT_ROLE() = 'HC_ANALYST' AND hospital_region IN ('North', 'South') THEN TRUE
        WHEN CURRENT_ROLE() = 'HC_VIEWER' AND hospital_region = 'North' THEN TRUE
        ELSE TRUE  -- Allow all for demo; customize per requirements
    END
    COMMENT = 'Region-based row access policy';

