    DATEADD(DAY, -UNIFORM(0, 180, RANDOM()), CURRENT_DATE) AS BILL_DATE,
    CASE UNIFORM(1,3,RANDOM())
        WHEN 1 THEN 'PAID'
        WHEN 2 THEN 'PENDING'
        ELSE 'OVERDUE'
    END AS PAYMENT_STATUS,
